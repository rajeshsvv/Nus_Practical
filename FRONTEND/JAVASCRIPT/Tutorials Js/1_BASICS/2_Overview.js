/*JavaScript is a dynamic computer programming language. 
It is lightweight and most commonly used as a part of web pages, 
whose implementations allow client-side script to interact with the user and make dynamic pages. 
It is an interpreted programming language with object-oriented capabilities.

The ECMA-262 Specification defined a standard version of the core JavaScript language.
JavaScript is a lightweight, interpreted programming language.
Designed for creating network-centric applications.
Complementary to and integrated with Java.
Complementary to and integrated with HTML.
Open and cross-platform

Client-side JavaScript is the most common form of the language. 
The script should be included in or referenced by an HTML document for the code 
to be interpreted by the browser.

It means that a web page need not be a static HTML, 
but can include programs that interact with the user, control the browser, 
and dynamically create HTML content.

The JavaScript client-side mechanism provides many advantages over traditional CGI server-side scripts. 
For example, you might use JavaScript to check 
if the user has entered a valid e-mail address in a form field.

The JavaScript code is executed when the user submits the form, 
and only if all the entries are valid, they would be submitted to the Web Server.

JavaScript can be used to trap user-initiated events such as button clicks, 
link navigation, and other actions that the user initiates explicitly or implicitly.


                                    Advantages of JavaScript

The merits of using JavaScript are −

Less server interaction − 
You can validate user input before sending the page off to the server.
 This saves server traffic, which means less load on your server.

Immediate feedback to the visitors − 
They don't have to wait for a page reload to see if they have forgotten to enter something.

Increased interactivity − You can create interfaces that react 
when the user hovers over them with a mouse or activates them via the keyboard.

Richer interfaces − You can use JavaScript to include such items as drag-and-drop components and 
sliders to give a Rich Interface to your site visitors.

                                    Limitations of JavaScript:
                                    
We cannot treat JavaScript as a full-fledged programming language. 
It lacks the following important features −

Client-side JavaScript does not allow the reading or writing of files. 
This has been kept for security reason.

JavaScript cannot be used for networking applications because there is no such support available.

JavaScript doesn't have any multi-threading or multiprocessor capabilities.

Once again, JavaScript is a lightweight, interpreted programming language 
that allows you to build interactivity into otherwise static HTML pages.
*/

